package tuple

/////////////////////////////////////////////////////////////////////////////
// An atom
// - a name for something, an identifier or operator
/////////////////////////////////////////////////////////////////////////////

type Atom struct {
	Name string
}

/////////////////////////////////////////////////////////////////////////////
// Tuple
/////////////////////////////////////////////////////////////////////////////

type Tuple struct {
	List []interface{}
}

func (tuple *Tuple) Append(token interface{}) {
	tuple.List = append(tuple.List, token)
}

func NewTuple() Tuple {
	return Tuple{make([]interface{}, 0)}
}

